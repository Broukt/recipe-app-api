"""
Calculator functions
"""


def add(x: int, y: int) -> int:
    """
    Add two numbers x and y and return result.

    Args:
        x (int): First number to sum.
        y (int): Second number to sum.

    Returns:
        int: Operation result.
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    Subtract x from y and return result.

    Args:
        x (int): First number to subtract
        y (int): Second number to subtract

    Returns:
        int: Operation result
    """
    return x - y
